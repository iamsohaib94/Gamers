            <p>
              Life Is Strange is an episodic graphic adventure video game developed by Dontnod Entertainment and published by Square Enix for Microsoft Windows, PlayStation 3, PlayStation 4, Xbox 360, and Xbox One. The game was released in five episodes periodically throughout 2015. The game's plot focuses on Max Caulfield, an 18-year-old photography student who discovers that she has the ability to rewind time at any moment, leading her every choice to enact the butterfly effect. After having foreseen an approaching storm, Max must take on the responsibility to prevent it from destroying her town. The player's actions will adjust the narrative as it unfolds, and reshape it once allowed to travel back in time. Fetch quests and making environmental changes represent the forms of puzzle solving in addition to using branching choices for conversation. Life Is Strange is a graphic adventure played from a third-person view. The mechanic of rewinding time allows the player to redo any action that has been taken. The player can examine and</p>
              <p>interact with objects, which enables puzzle solving in the form of fetch quests and making changes to the environment. Items that are collected before time travelling will be kept in the inventory after the fact. The player can explore various locations in the fictional setting of Arcadia Bay and communicate with non-playable characters. Dialogue exchanges can be rewound while branching options are used for conversation. Once an event is reset, the details provided earlier are permitted to avail themselves in the future. In some instances, choices in dialogue will alter and affect the story through short or long-term consequences. For each one of the choices, something good in the short-term could turn out worse later. Life Is Strange is set in the fictional town of Arcadia Bay, Oregon, and is told from the perspective of Maxine "Max" Caulfield (Hannah Telle), a twelfth grade student attending Blackwell Academy during October 2013. During photography class with her teacher Mark Jefferson (Derek Phillips), Max experiences a</p>
              <p>vision of a lighthouse being destroyed by a swelling tornado. Leaving for the restroom to regain her composure, she witnesses classmate Nathan Prescott (Nik Shriner) kill a woman in a fit of rage. In a single, sudden effort, she develops the ability to rewind time and rescues the girl, revealed to be her childhood friend Chloe Price (Ashly Burch). The two reunite and go for a walk at the lighthouse, where Max reveals to Chloe her capacity to travel back in time. It is established that the vision is rather the reckoning of a future event: a storm approaching the town. The next day, Max observes fellow student Kate Marsh (Dayeanne Hutton) being bullied for a viral video depicting her kissing several students at a party.
