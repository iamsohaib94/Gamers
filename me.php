          <h2 class="mt-0">Short Description</h2>
          <div class="description">
            <p>
              Remember Me is a cyberpunk action-adventure video game developed by Dontnod Entertainment and published by Capcom. It was released worldwide in June 2013 for Microsoft Windows, PlayStation 3 and Xbox 360. The game's plot focuses on Nilin, a memory hunter working for an underground resistance called the Errorists. When the game starts, she has been stripped of nearly all her memories by megacorporation Memorize. With the help of a mysterious man named Edge, she goes on a quest to bring down Memorize and recover her lost memories. Throughout the story, she is permitted to use her Memory Remix power to ultimately refurbish people's recollections. The combat consists of a modified combo system called Pressen. Remember Me was developed as the debut project of Dontnod Entertainment, with one of the company's founding members Jean-Maxime Moris as its director. Part of his goal for the game was to create a thought-provoking narrative, and eventually settled on a female protagonist to help convey the story's themes.</p>
              <p>Originally a PlayStation 3-exclusive under Sony titled Adrift, it was cancelled in 2011 and later purchased by Capcom which resurrected it as a multiplatform game. General praise was given for the world design, Olivier Deriviere's soundtrack, the ambition of the story and the Memory Remix segments, while the main criticisms laid against other aspects of the story, poor design choices and formulaic combat. Remember Me features platforming, exploration and melee combat. The game is played as Nilin from a third-person view and introduces the mechanic of memory remixing: entering and rearranging a target's memories to manipulate them. Players accomplish this by replaying a memory and modifying details to change the target's recollection of the outcome. Another key mechanic of the gameplay is stealing memories from certain targets and using points called Remembranes to replay the memory in real-time: this is often needed to proceed through the game or avoid hazards otherwise hidden from the player.
            </p>
