            <p>
              Project CARS is a motorsport racing simulator video game developed by Slightly Mad Studios and published and distributed by Bandai Namco Entertainment. It was released in May 2015 for Microsoft Windows, PlayStation 4, and Xbox One. There are 74 drivable cars, over 30 unique locations with at least 110 different courses, of which 23 are real, with the remainder being fictional. For licensing reasons, some tracks are codenamed using their geographic location. In addition to real world racing circuits and fictional kart circuits, there are two fictional point-to-point roads inspired by CÃ´te d'Azur and California Pacific Coast. The game adopts an improved version of the Madness engine, which was the basis for the Need for Speed: Shift titles. More processing power available in modern computers allows for the introduction of a dynamic tire model named "SETA", rather than the steady-state model based on lookup tables, as seen in previous generation simulations. To accommodate differing skill levels, Slightly Mad Studios<br><br>
              offers gamers (with or without a digital wheel) various driver aids and input filtering methods. Project CARS, which stands for Community Assisted Racing Simulator, was made with a total sum of $5 million. Funding for the game was raised by the community and the developers themselves, without the financial aid of a traditional publisher. Through the purchase of Tool Packs players could contribute to development in roles including content creation, QA, and marketing media. Members gain special perks, depending on their purchased tool pack. Members will receive a share of game sales profits generated within the first three years after launch as compensation for their efforts, to be paid quarterly. The game was supported with both paid and free downloadable content (DLC) upon release. Project CARS was initially planned to follow the season-pass model but plans for that were replaced with a system called "On-Demand." On-Demand was described as allowing a "commitment to keeping Project CARS up-to-date with the greatest,<br><br>
              freshest, and most critical content whilst also allowing players to pick and choose the cars & tracks they want - without being locked in to a pre-paid scheme." The first DLC was the Limited Edition Upgrade, released in June 2015, which added five cars already featured in the Project CARS Limited Edition. It was followed by the Racing Icons Car Pack, Modified Car Pack (which was also available as bonus content for the pre-order version of the game) and Old vs New Car Pack, all of which added new cars to the game. Still in 2015, the developers released the Audi Ruapuna Speedway Expansion, Aston Martin Track Expansion and the Classic Lotus Track Expansion which added new cars and four new racetracks: Mike Pero Motorsport Park, the historic version of Hockenheimring and Silverstone, Rouen-Les-Essarts and a fictional track called "Mojave Test Track".
