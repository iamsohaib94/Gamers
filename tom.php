            <p>
             Tom Clancy's Ghost Recon Wildlands (commonly referred to as Ghost Recon Wildlands) is a tactical shooter video game developed by Ubisoft Paris and published by Ubisoft. It was released worldwide on March 7, 2017, for Microsoft Windows, PlayStation 4 and Xbox One, as the tenth installment in the Tom Clancy's Ghost Recon franchise and is the first game in the Ghost Recon series to feature an open world environment. The game moves away from the futuristic setting introduced in Tom Clancy's Ghost Recon Advanced Warfighter and instead features a setting similar to the original Tom Clancy's Ghost Recon. Ubisoft described it as one of the biggest open world games that they have published, with the game world including a wide variety of environments such as mountains, forests, deserts, and salt flats. Tom Clancy's Ghost Recon Wildlands is a tactical shooter game set in an open world environment and played from a third-person perspective with an optional first person view for gun aiming. Players play as members of the<br><br>
             Delta Company, First Battalion, 5th Special Forces Group, also known as "Ghosts", a fictional elite special operations unit of the United States Army under the Joint Special Operations Command. It does not feature the futuristic setting used in Advanced Warfighter and Future Soldier, but instead adopts a modern-day setting, similar to the original Tom Clancy's Ghost Recon. As a result, the equipment featured in the game is based on weapons and gear commonly used by military forces around the world. However, it features some original equipment, such as drones that can be used to tag enemies and show objectives. These drones have limited abilities until upgraded. The game is the first entry to feature an open world environment, which consists of nine different types of terrain, such as: mountains, forests, desert, salt flats, and also introduces a dynamic weather system as well as a day-night cycle. Completing missions during day-time allows players to spot enemies easily, while completing missions at night grants players<br><br>
             a tactical advantage, as the night offers players better concealment and easier infiltration due to some guardsmen being asleep. Players are tasked with making observations before carrying out missions. A variety of vehicles, such as dirt bikes, helicopters and dune buggies are featured in the game. Unlike its predecessors, Wildlands features several side-missions. The game takes place in Bolivia in July 2019. The country has become increasingly unstable as a Mexican drug cartel known as the Santa Blanca Cartel gains more power and influence within the country and turns Bolivia into the world's largest producer of cocaine. The United States is compelled to act when a bomb targets the US embassy in La Paz and undercover DEA agent Ricardo "Ricky" Sandoval is executed by the cartel. A Ghost Recon fireteam is deployed to Bolivia as part of Operation Kingslayer, a joint operation between the CIA, DEA, and JSOC. The team consists of team leader and support gunner "Nomad", vehicle and assault specialist<br><br>
             "Midas", hacker and tactical engineer "Holt", and sniper "Weaver". The Ghosts enter Bolivia with their CIA contact, Karen Bowman, who was also a close friend to Sandoval. They meet Pac Katari, leader of the Kataris 26, the only resistance against Santa Blanca. Pac Katari requests they rescue Amaru, whose ideologies inspired the Kataris 26, from Santa Blanca. The Ghosts are then free to tackle the cartel in any way and order they see fit.
