          <h2 class="mt-0">Short Description</h2>
          <div class="description">
            <p>
              Outlast is a first-person survival horror video game developed and published by Red Barrels. The game revolves around a freelance investigative journalist, Miles Upshur, who decides to investigate a remote psychiatric hospital named Mount Massive Asylum, located deep in the mountains of Lake County, Colorado. The downloadable content, Outlast: Whistleblower, centers on Waylon Park, the man who led Miles there in the first place. Outlast received generally positive reviews from critics, with praise directed at its atmosphere, horror elements and gameplay. As of October 2016, the game has sold four million copies overall. In Outlast, the player assumes the role of investigative journalist Miles Upshur, as he navigates a dilapidated psychiatric hospital in Leadville, Colorado that is overrun by homicidal patients. The game is played from a first-person perspective and features some stealth gameplay mechanics. The player can walk, run, crouch, jump, climb ladders and vault over objects. Unlike most games, however, the player doesn't</p>
              <p>have a visible health bar on the screen and is unable to attack enemies. The player must instead rely on stealth tactics such as hiding in lockers, sneaking past enemies, staying in the shadows and hiding behind or under things in order to survive. Alternatively, the player can attempt to outrun their pursuer. If the player dies, the game will reset to the most recent checkpoint. Most of the hospital is unlit, and the only way for the player to see while in the dark is through the lens of a camcorder equipped with night vision. Using the night vision mode will slowly consume batteries, forcing the player to scavenge for additional batteries found throughout the building. Outlast makes heavy use of traditional jump scares and audio cues, which alert the player if an enemy has seen them. If the player records specific events with their camcorder, Miles will write a note about it, providing further insight into his thoughts. Documents can be collected, which offer backstory and other supplementary information about the hospital.</p>
              <p>Developer Red Barrels have pointed to the survival-focused gameplay in Amnesia: The Dark Descent as a primary influence on the combat-free narrative style of Outlast. Found-footage horror films like REC also served as influences. Freelance investigative journalist Miles Upshur receives an anonymous email that inhumane experiments are being conducted at Mount Massive Asylum, a private psychiatric hospital owned by the notoriously unethical Murkoff Corporation. Upon entering, Miles is shocked to discover its halls ransacked and littered with the mutilated corpses of the staff. He is informed by a dying SWAT officer that Mount Massive's deranged inmates, known as "variants", have escaped and are freely roaming the grounds, butchering Murkoff's employees. The officer implores him to leave, but Miles finds he cannot return the way he came and must press on.
            </p>
